// TODO remove, this demo shouldn't need to reset the theme.

const defaultTheme = createTheme();

export default function LogIn() {
  const navigate=useNavigate()
  const handleSubmit = async(event) => {
    event.preventDefault();
    try{
      const data = new FormData(event.currentTarget);
    const logindata=await axios.post("https://todo-backend-ay8w.onrender.com/api/v1/auth/login",{
      Email: data.get('email'),
      Password: data.get('password'),
    })
    alert(logindata.data.message)
    if(logindata.data.token!=null){
      localStorage.setItem("Token",logindata.data.token)
      localStorage.setItem("User",logindata.data.user.FirstName+" "+logindata.data.user.LastName);
      navigate("/")
    }else{
      navigate("/SignUp")
    }
    }catch(err){
    }
  };
  return (
    <>
    <ThemeProvider theme={defaultTheme}>
      <Container component="main" maxWidth="xs">
        <CssBaseline />
        <Box
          sx={{
            marginTop: 8,
            display: 'flex',
            flexDirection: 'column',
            alignItems: 'center',
          }}
        >
          <Avatar sx={{ m: 1, bgcolor: 'secondary.main' }}>
            <LockOutlinedIcon />
          </Avatar>
          <Typography component="h1" variant="h5" fontWeight="bold">
            Log In
          </Typography>
          <Box component="form" onSubmit={handleSubmit} noValidate sx={{ mt: 1 }}>
            <TextField
              margin="normal"
              required
              fullWidth
              id="email"
              label="Email Address"
              name="email"
              autoComplete="email"
              autoFocus
            />
            <TextField
              margin="normal"
              required
              fullWidth
              name="password"
              label="Password"
              type="password"
              id="password"
              autoComplete="current-password"
            />
            <Button
              type="submit"
              fullWidth
              variant="contained"
              sx={{ mt: 3, mb: 2 }}
            >
              Sign In
            </Button>
            <Grid container justifyContent="flex-center">
              <Grid item>
                <Link variant="body2" onClick={()=>{navigate('/SignUp')}} style={{ cursor: 'pointer' }}>
                Don't have an account? Sign Up
                </Link>
              </Grid>
            </Grid>
          </Box>
        </Box>
        <Copyright sx={{ mt: 8, mb: 4 }} />
      </Container>
    </ThemeProvider>
    </>
  );
}